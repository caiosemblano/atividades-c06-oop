package ExerciciosDeSala.capitulo3.exercicio6;

public class Driver {
    private String name;
    private boolean villain;

    public void soltaSuperPoder() {
    }

    public Driver() {
    }

    public Driver(String name, boolean villain) {
        this.name = name;
        this.villain = villain;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isVillain() {
        return villain;
    }

    public void setVillain(boolean villain) {
        this.villain = villain;
    }
}