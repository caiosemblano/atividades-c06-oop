package ExerciciosDeSala.capitulo4.Exercicio1;

public class Empresario {
    private String nome;
    private Long cnpj;

    public Empresario(String nome, Long cnpj) {
        this.nome = nome;
        this.cnpj = cnpj;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Long getCnpj() {
        return cnpj;
    }

    public void setCnpj(Long cnpj) {
        this.cnpj = cnpj;
    }
}
